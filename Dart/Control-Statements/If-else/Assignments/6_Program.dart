void main() {
    int val = 4;
    if(val == 0) {

        print("Zero");
    } else if(val == 1) {

        print("One");
    } else if(val == 2) {

        print("Two");
    } else if(val == 3) {

        print("Three");
    } else if(val == 4) {

        print("Four");
    } else if(val == 5) {

        print("Five");
    } else {

        print("Number is Greater than 5");
    }
}